namespace TrackLines\AbstractEntity;

/**
 * Class AccountAbstract
 * @package TrackLines\AbstractEntity
 */
abstract class AccountAbstract
{
    /**
     * @var string
     */
    private $username;

    /**
     * @var string
     */
    private $password;

    /**
     * @var string
     */
    private $email;

    /**
     * @var string
     */
    private $contactName;

    /**
     * @var int
     */
    private $accountId;

    /**
     * @var int
     */
    private $parentAccountId;

    /**
     * @return string
     */
    public function getUsername(): string
    {
        return $this->username;
    }

    /**
     * @param string $username
     */
    public function setUsername(string $username)
    {
        $this->username = $username;
    }

    /**
     * @return string
     */
    public function getPassword(): string
    {
        return $this->password;
    }

    /**
     * @param string $password
     */
    public function setPassword(string $password)
    {
        $this->password = $password;
    }

    /**
     * @return string
     */
    public function getEmail(): string
    {
        return $this->email;
    }

    /**
     * @param string $email
     */
    public function setEmail(string $email)
    {
        $this->email = $email;
    }

    /**
     * @return string
     */
    public function getContactName(): string
    {
        return $this->contactName;
    }

    /**
     * @param string $contactName
     */
    public function setContactName(string $contactName)
    {
        $this->contactName = $contactName;
    }

    /**
     * @return int
     */
    public function getAccountId(): int
    {
        return $this->accountId;
    }

    /**
     * @param int $accountId
     */
    public function setAccountId(int $accountId)
    {
        $this->accountId = $accountId;
    }

    /**
     * @return int
     */
    public function getParentAccountId(): int
    {
        return $this->parentAccountId;
    }

    /**
     * @param int $parentAccountId
     */
    public function setParentAccountId(int $parentAccountId)
    {
        $this->parentAccountId = $parentAccountId;
    }


}
